    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        int sel = table.getSelectedRow();
        String id = tabModel.getValueAt(sel, 0).toString();
        int ID = Integer.parseInt(id);
        try {
            String click1 = "select * from transaction where id = '" +ID+ "'";
            rs = stm.executeQuery(click1);
            while(rs.next()) {
                String nama = rs.getString("nama");
                String nik = rs.getString("nik");
                String alamat = rs.getString("alamat");
                String telpon = rs.getString("telpon");
                String email = rs.getString("email");
                String nopol = rs.getString("nopol");
                String tgll1 = rs.getString("tgl_pinjaman");
                String tgll2 = rs.getString("tgl_kembali");
                String durasi = rs.getString("durasi")+" hari";
                String total = rs.getString("total")+" rupiah";
                
                namaField.setText(nama);
                nikField.setText(nik);
                alamatField.setText(alamat);
                telponField.setText(telpon);
                emailField.setText(email);
                nama2Field.setText(nama);
                nopolField.setText(nopol);
                tgl1.setText(tgll1);
                tgl2.setText(tgll2);
                durasiField.setText(durasi);
                totalField.setText(total);
            }
        }catch(SQLException e) {
            JOptionPane.showMessageDialog(null, "Gagal");
        }
    }//GEN-LAST:event_tableMouseClicked

