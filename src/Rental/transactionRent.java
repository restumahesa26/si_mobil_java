    private void tampil_data() {
        Object baris[] = {"Id", "Nama", "NIK", "Alamat", "No. Telpon", "Email"};
        tabModel = new DefaultTableModel(null, baris);
        table.setModel(tabModel);
        try {
            con = new koneksilogin().ConnectDB();
            String select = "select * from transaction";
            stm = con.createStatement();
            rs = stm.executeQuery(select);
            while(rs.next()) {
                String id = rs.getString("id");
                String nama = rs.getString("nama");
                String nik = rs.getString("nik");
                String alamat = rs.getString("alamat");
                String telpon = rs.getString("telpon");
                String email = rs.getString("email");
                String[] data = {id, nama, nik, alamat, telpon, email};
                tabModel.addRow(data);
            }
        }catch(SQLException e) {
            
        }
    }
    
    
